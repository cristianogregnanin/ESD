/* Scrivete un programma che dimostri la differenza tra il predecremento e il postdecremento,
utilizzando l'operatore di decremento --. */

#include <stdio.h>

/*inizio del programma */
int main()
{

int a;

a = 2;
printf("%d\n", a--);

a = 2;
printf("%d\n", --a);


return 0;
} /*fine del programma*/
